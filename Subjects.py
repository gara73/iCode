class Subject:
    __name = ""
    __teacher = ""
    __hours = 0


    def set_name(self, name):
        self.__name = name


    def get_name(self):
        return self.__name


    def set_teacher(self, teacher):
        self.__teacher = teacher


    def get_teacher(self):
        return self.__teacher


    def set_hours(self, hours):
        self.__hours = hours

    def get_hours(self):
        return self.__hours